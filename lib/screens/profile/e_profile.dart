                            shape: BoxShape.circle,
                            border: Border.all(
                              width: 4,
                              color: Colors.white,
                            ),
                            color: Colors.blue,
                          ),
                          child: const Icon(
                            Icons.edit,
                            color: Colors.white,
                          ),
                        ),
                      ),
                    ],
                  ),
                ),
                const SizedBox(
                  height: 30,
                ),
                buildTextField("Full Name", 'Arthur', false),
                buildTextField("Email", 'ja@gmail.com', false),
                buildTextField("Password", '*******', true),
                buildTextField("Location", 'Nairobi', false),
                const SizedBox(
